		StringTokenizer st = new StringTokenizer(busqueda, " ");
		int i = 1;
		while (st.hasMoreElements()) {
			if (i != 1)
				query.append(" OR ");
			query.append("R.PRODUCTO LIKE '%" + st.nextElement() + "%' ");
			i++;
		}
		query.append(" ) AND (T.EMPRESA LIKE '%"
				+ (Null.free(tractoraReq).equals("-1") ? "" : tractoraReq)
				+ "%' ");
